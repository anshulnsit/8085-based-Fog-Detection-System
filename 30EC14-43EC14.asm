MAIN:	LXI SP,0FFFFH
	CALL LCD_INIT	;Initialize LCD
	CALL LCD_COFF	;Turn off cursor of LCD
	MVI A,0FH	;Mask all Interrupts
	SIM
	CALL WELCOME	;Display welcome screen
	CALL DELAY_1	;1 sec delay
